    <h2>What is Net Neutrality? </h2>

    <p>Simply put, Net Neutrality allows users across the internet to be free. With Net Neutrality, users can browse the web equally, which is a good thing. The problem is, Internet Service Providers (ISPs) like Comcast and Verizon want to prevent equality and freedom across the web. If Comcast and Verizon win the battle, they could theoretically do the following:</p>

    <ul>
      <li>Slow down Internet speeds to your favorite sites. (Netflix, YouTube, Twitter, Facebook, iOS Haven üòè, etc.) </li>
      <li>Block content.</li>
      <li>Create "Fast Lanes". Meaning you have to pay extra to access specific sites or get decent Internet speeds.</li>
    </ul>

